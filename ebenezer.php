              Ebenezer Nursery and primary school was started by Pentecostal Assemblies of God Otubanga church in Obongi
              District, West Nile region of Uganda.
              The main aim of starting this school is to address the low literacy and numeracy proficiency rates of the
              learners in Aliba Sub County, where the church is situated. The whole sub county lacked an early childhood
              development school.
              Being a fishing community, parents could not afford to transport their children 12km away to the nearest ECD
              School, leave alone their inability to pay for school fees.
              The church took this as an opportunity to address this need and use this school as a ministry ground for
              discipleship of children.
              The school started in 2017 with 97 children from baby to top class. Today there are 365 children in the
              school, from baby class to p2.
              This population is only 15% of the early childhood school going age children in the parish where the school
              sits. Due to limited classrooms and sanitary facilities (only three locally constructed stance of latrine)
              we cannot enroll more than the current number. The school only has four temporary rooms against the above
              number, which rooms can definitely not accommodate many learners taking into consideration the SOPS for
              Covid19. Some learners study under trees and are forced to miss lessons during rainy days
              Ebenezer Nursery and primary school sits on approximately 2.5 hectares of land with high possibility of
              expansion. There are eleven teachers in the school. Nine teachers in the Nursery section and two in the
              primary section
